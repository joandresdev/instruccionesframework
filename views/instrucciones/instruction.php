						<p><b>1er parametro:</b> es como accedere, es decir la ruta de la url es decir si yo entro a www.see.edu/<code>rutadecomoquieroacceder</code> debes usar nombres relacionados con tus metodos, acciones y modulos, es decir no pongas <code>www.see.edu/eliminaralumno</code> cuando quieres redireccionar a un controlador para crear uno, este parametro se conoce como <code>slug</code></p>
						<div class="white-space-16"></div>
						<p><b>2do parametro:</b> ruta del archivo, dentro de la carpeta controller ¿donde esta? si yo tengo mi controlador dentro de controllers/<code>welcome/welcomeController.php</code> entonces la ruta queda <code>welcome/welcomeController</code></p>
						<div class="white-space-16"></div>
						<p><b>3er parametro:</b> Es el metodo el cual quieres ejecutar (de tu controlador)</p>
						<div class="white-space-16"></div>
